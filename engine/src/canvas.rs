use Bitmap;

//画布
pub trait Canvas {
    fn draw_image_repeat(&self, image: &Bitmap, x: i32, y: i32, width: i32, height: i32);
    fn draw_image_repeat_x(&self, image: &Bitmap, x: i32, y: i32, width: i32, height: i32);
    fn draw_image_repeat_y(&self, image: &Bitmap, x: i32, y: i32, width: i32, height: i32);
    fn draw_image_at(&self, image: &Bitmap, x: i32, y: i32);
    fn draw_image(
        &self,
        image: &Bitmap,
        source_x: i32,
        source_y: i32,
        source_width: i32,
        source_height: i32,
        dest_x: i32,
        dest_y: i32,
        dest_width: i32,
        dest_height: i32,
    );
    fn line_width(&self, width: i32);
    fn set_font(&self, font: &str);
    fn fill_style(&self, style: &str);
    fn stroke_style(&self, style: &str);
    fn fill_rect(&self, x: i32, y: i32, width: i32, height: i32);
    fn stroke_rect(&self, x: i32, y: i32, width: i32, height: i32);
    fn fill_text(&self, text: &str, x: i32, y: i32);
}
